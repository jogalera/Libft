#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*aux;

	aux = (char *)malloc(ft_strlen(s1) + 1);
	if (!aux)
		return (0);
	i = 0;
	while (s1[i])
	{
		aux[i] = s1[i];
		i++;
	}
	aux[i] = '\0';
	return (aux);
}
